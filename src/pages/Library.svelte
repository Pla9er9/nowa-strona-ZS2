    <p>W czerwcu 2018 r. nasza szkoła otrzymała 15.000 złotych na zakup książek do Biblioteki Szkolnej w ramach Narodowego Programu Rozwoju Czytelnictwa na lata 2016-2020. Priorytet 3: Rozwijanie zainteresowań uczniów przez promowanie i wspieranie rozwoju czytelnictwa wśród dzieci i młodzieży, w tym zakup nowości wydawniczych.</p>

    <p>Warunkiem przyznania wsparcia było zapewnienie przez organ prowadzący szkołę co najmniej 20% wkładu własnego na realizację zakupów. Otrzymaliśmy wsparcie finansowe dla Biblioteki Szkolnej w kwocie 15.000 złotych, z czego 12.000 pochodzi z dotacji, a 3.000 to wkład własny otrzymany od Powiatu Wągrowieckiego.</p>

    <p>Za przyznaną kwotę zostało zakupionych 567 książek (fantastyka, kryminały, sensacje, horrory, powieści obyczajowe, biografie, książki w języku angielskim itp. oraz lektury, które znajdą się w nowej podstawie programowej), a także 12 audiobooków. Uwzględniono również potrzeby uczniów z niepełnosprawnościami.</p>

    <p>W wyniku realizacji projektu księgozbiór Biblioteki Szkolnej znacznie się wzbogacił. Przy zakupie nowości wydawniczych uwzględniono życzenia uczniów i nauczycieli, a regały z nowymi książkami cieszą się ogromną popularnością wśród czytelników.</p>

    <p>Obok zakupu książek zaplanowano szereg inicjatyw promujących czytelnictwo w szkole oraz w środowisku lokalnym.</p>

